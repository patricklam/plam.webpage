<p>I also have
 worked on <a
 href="/papers/RoleAnalysis.ps.gz"> role
 analysis</a> with <a href="http://www.mit.edu/people/vkuncak">Viktor
 Kuncak</a>; roles declare the aliasing structure of the objects
 participating in heap data structures, particularly information about
 outgoing fields and incoming slots of an object. &nbsp;These are used
 to verify that a program conforms to its role declarations; the paper
 contains a role specification language and a compositional
 interprocedural analysis for verifying role annotations.</p>

