<p>In my <a href="/sase">Static Analysis for Software Engineering</a>
graduate course, each student is expected to present 2 papers to the class.
Here are some recommendations on how to give a good paper presentation.</p>

<ul>
<li><b>Avoid bullets.</b> When appropriate, diagrams are way better than
bullet points for explaining concepts. Just because PowerPoint (or clone)
makes it easy to write bullet points doesn't mean that you should use
bullet points.</li>
<li><b>Go concrete, not abstract.</b> In fact, if you can explain the main
idea of a paper with an example, focus on the example first and then summarize
the key ideas. (This works better with concepts, not experimental results.)</li>
<li><b>Be choosy.</b> You don't need to present the whole paper. Just present
the most interesting ideas. There's no need to be exhaustive. The paper contains
everything, after all. You're just going over the highlights.</li>
<li><b>Think about charts.</b> Try to make charts that tell a story and
choose the charting technology that best supports that. (See 
<a href="http://edwardtufte.com">Edward Tufte</a>'s
books.)</li>
</ul>

<p>Also check out the <a href="http://www.presentationzen.com/presentationzen/2005/10/the_lessig_meth.html">Lessig style</a> of giving talks.</p>

